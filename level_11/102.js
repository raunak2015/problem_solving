// Q102. Sum of Two Numbers
// Write a function that takes two numbers and returns their sum.

// Input: a = 5, b = 7 → Output: 12
// Input: a = -2, b = 10 → Output: 8
// Input: a = 0, b = 0 → Output: 0

function sum(num1,num2){
    return num1+num2;
}

console.log(sum(5,7));
console.log(sum(-2,10));
console.log(sum(0,0));