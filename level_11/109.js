// Q109. First Character of String
// Write a function that returns the first character of a string.

// Input: "hello" → Output: "h"
// Input: "A" → Output: "A"
// Input: "js" → Output: "j"

function first(str){
    return str[0];
}

console.log(first("hello"));
console.log(first("A"));
console.log(first("hello"));