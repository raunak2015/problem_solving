
// Q107. Is First Number Greater Than Second
// Write a function that takes two numbers and returns true if first > second.

// Input: a = 10, b = 5 → Output: true
// Input: a = 3, b = 9 → Output: false
// Input: a = 7, b = 7 → Output: false

function greater(num1,num2){
    return num1>num2;
}

console.log(greater(10,5));
console.log(greater(3,9));
console.log(greater(7,7));