// Q115. Hours to Minutes
// Write a function that converts hours to minutes.

// Input: hours = 1 → Output: 60
// Input: hours = 2.5 → Output: 150
// Input: hours = 0 → Output: 0

function converts(hr){
    return 60*hr;
}

console.log(converts(1));
console.log(converts(2.5));
console.log(converts(0));